export type TranslationKey = keyof typeof translations.ua;

export function t(lang: Lang, key: TranslationKey): string {
  return translations[lang][key] ?? translations.ua[key] ?? key;
}

export function localePath(lang: Lang, path: string): string {
  return `/${lang}${path.startsWith('/') ? path : '/' + path}`;
}

export function switchLangPath(currentPath: string, targetLang: Lang): string {
  const stripped = currentPath.replace(/^\/(ua|en)/, '');
  return `/${targetLang}${stripped || '/'}`;
}
